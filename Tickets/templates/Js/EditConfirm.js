document.addEventListener('DOMContentLoaded',function (){
    const editBox = document.querySelectorAll('.edit-confirm');

    editBox.forEach(link => link.addEventListener('click',function (e){
        e.preventDefault();
        const editConfirmation = confirm("Are you sure to UPDATE this Ticket ???");

        if (editConfirmation){
            window.location.href = this.href
        }
    }))
})